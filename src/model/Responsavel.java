package model;

public class Responsavel {
	
	//Atributos do modelo
	private int numTarefa;
	private int cpf;
	private int mes;
	private int ano;
	
	//Getters e Setters
	public int getNumTarefa() {
		return numTarefa;
	}
	public void setNumTarefa(int numTarefa) {
		this.numTarefa = numTarefa;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCPF(int cpf) {
		cpf = cpf;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	
	

}
